# Olympic rings

from signal import pause
import turtle
t = turtle.Pen()
t.width(10)
t.color("blue")
t.circle(100)
t.penup()
t.goto(240, 0)
t.pendown()
t.color("black")
t.circle(100)
t.penup()
t.goto(480, 0)
t.pendown()
t.color("red")
t.circle(100)
t.penup()
t.goto(120, -100)
t.pendown()
t.color('yellow')
t.circle(100)
t.penup()
t.goto(360, -100)
t.pendown()
t.color('green')
t.circle(100)